  // === FUN√á√ÉO AUXILIAR PARA EXTRAIR JSON ===
  extrairJSON(texto) {
    if (!texto || typeof texto !== 'string') {
      throw new Error('Texto inv√°lido para extrair JSON');
    }
    
    console.log(`   üîç ATACADO: Tentando extrair JSON de: "${texto.substring(0, 100)}..."`);
    
    try {
      // Tentativa 1: Parse direto
      return JSON.parse(texto);
    } catch (e) {
      console.log(`   ‚ö†Ô∏è ATACADO: Parse direto falhou, tentando limpar...`);
      
      try {
        // Tentativa 2: Remover markdown e limpar
        let limpo = texto
          .replace(/```json\n?/g, '')
          .replace(/```\n?/g, '')
          .replace(/^```json/g, '')
          .replace(/```$/g, '')
          .trim();
        
        // Remover poss√≠veis caracteres especiais no in√≠cio/fim
        limpo = limpo.replace(/^[^\{\[]*/, '').replace(/[^\}\]]*$/, '');
        
        console.log(`   üîç ATACADO: Texto limpo: "${limpo.substring(0, 100)}..."`);
        
        return JSON.parse(limpo);
      } catch (e2) {
        console.log(`   ‚ö†Ô∏è ATACADO: Limpeza falhou, tentando regex...`);
        
        try {
          // Tentativa 3: Extrair com regex
          const match = texto.match(/\{[\s\S]*\}/);
          if (match) {
            console.log(`   üîç ATACADO: JSON encontrado com regex: "${match[0].substring(0, 100)}..."`);
            return JSON.parse(match[0]);
          }
          
          // Tentativa 4: Procurar por arrays tamb√©m
          const matchArray = texto.match(/\[[\s\S]*\]/);
          if (matchArray) {
            console.log(`   üîç ATACADO: Array encontrado com regex: "${matchArray[0].substring(0, 100)}..."`);
            return JSON.parse(matchArray[0]);
          }
          
        } catch (e3) {
          console.log(`   ‚ùå ATACADO: Regex tamb√©m falhou`);
        }
        
        // Tentativa 5: Limpeza mais agressiva
        try {
          let textoLimpo = texto
            .replace(/[^\{\}\[\]",:0-9a-zA-Z\s\.\-]/g, '') // Manter apenas caracteres JSON v√°lidos
            .replace(/\s+/g, ' ') // Normalizar espa√ßos
            .trim();
          
          // Procurar por padr√µes JSON v√°lidos
          const jsonMatch = textoLimpo.match(/\{[\s\S]*\}/);
          if (jsonMatch) {
            console.log(`   üîç ATACADO: JSON limpo encontrado: "${jsonMatch[0].substring(0, 100)}..."`);
            return JSON.parse(jsonMatch[0]);
          }
          
        } catch (e4) {
          console.log(`   ‚ùå ATACADO: Limpeza agressiva tamb√©m falhou`);
        }
        
        // Se chegou aqui, n√£o foi poss√≠vel extrair
        throw new Error(`N√£o foi poss√≠vel extrair JSON v√°lido do texto: ${texto.substring(0, 200)}...`);
      }
    }
  }

